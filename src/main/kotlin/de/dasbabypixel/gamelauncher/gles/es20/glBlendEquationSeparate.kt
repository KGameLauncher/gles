    * 
    * @see de.dasbabypixel.gamelauncher.gles.es20.glBlendEquationSeparate.glBlendEquationSeparate
    * @see de.dasbabypixel.gamelauncher.gles.es32.glBlendEquationSeparatei.glBlendEquationSeparatei
    */
    fun glBlendEquationSeparate(modeRGB: Int, modeAlpha: Int)
}