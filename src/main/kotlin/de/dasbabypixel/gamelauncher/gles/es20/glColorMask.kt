    * 
    * @see de.dasbabypixel.gamelauncher.gles.es20.glColorMask.glColorMask
    * @see de.dasbabypixel.gamelauncher.gles.es32.glColorMaski.glColorMaski
    */
    fun glColorMask(red: Boolean, green: Boolean, blue: Boolean, alpha: Boolean)
